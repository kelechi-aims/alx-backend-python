#!/usr/bin/env python3
"""
Write a type-annotated function to_str that takes a float n as
argument and returns the string representation of the float.
"""


def to_str(n: float) -> str:
    """
    Returns the str representation of a floating-point number.

    Parameters:
    - n (float): The input floating-point number.

    Returns:
    str: The str representation of the input number.
    """
    return str(n)
